package planner;

/**
 * Represents type of plan: weekly, monthly or yearly.
 *
 * @author choondrise
 */
public enum PlanType {

    /**
     * Plan to be completed in current week.
     */
    WEEKLY,

    /**
     * Plan to be completed in current month.
     */
    MONTHLY,

    /**
     * Plan to be completed in current year.
     */
    YEARLY

}
